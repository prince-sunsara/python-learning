'''
INTRODUNCTION TO OBJECT ORIENTED PROGRAMMING:
    > In programming languages, mainly there are two approaches that are used to write program or code.
    1). Procedural Programming
        > The procedure we are following till now is the “Procedural Programming” approach.
    2). Object-Oriented Programming
        > The basic idea of object-oriented programming (OOP) in Python is to use classes and objects to represent real-world concepts and entities.
'''

'''
CLASS:
    > Blueprint or template for creating objects.
    > defines properties and methods that an object of the class will have.
    > properties are data or state of an object, and methods are the actions or behaviours that an object can perform.
'''

'''
OBJECT:
    > Object is instance of a class, and it contains its own data and methods. 
'''

'''
ENCAPSULATION:
    > it means that the interanal state of an object is hidden and can only be accessed or modified through the object's methods.
    > This helps to protect the object's data and prevent it from being modified in unexpected ways.
'''

'''
INHERITANCE:
    > It allows new classes to be created that inherit the properties and methods of an existing class. 
    > This allows for code reuse and makes it easy to create new classes that have similar functionality to existing classes.
'''

'''
POLYMORPHISM:
    > it means that objects of different classes can be treated as if they were objects of a common class.
    > This allows for greater flexibility in code and makes it easier to write code that can work with multiple types of objects.
'''
